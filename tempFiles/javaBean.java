public class javaBean
{
    private int javaData;
    private String unusedString;
    public javaBean()
    {
        javaData = 0;
    }
    public int getJavaData()
    {
        return javaData;
    }
    public void setJavaData(int javaData)
    {
        this.javaData = javaData;
    }
    public String getUnusedString()
    {
        return unusedString;
    }
    public void setUnusedString(String unusedString)
    {
        this.unusedString = unusedString;
    }
}